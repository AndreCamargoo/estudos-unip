# Adição
adicao = 10 + 10
print('Adição:', adicao)  # Resultado: 20

# Subtração
subtracao = 10 - 10
print('Subtração:', subtracao)  # Resultado: 0

# Multiplicação
multiplicacao = 10 * 10
print('Multiplicação:', multiplicacao)  # Resultado: 100

# Divisão
divisao = 10 / 3  # A divisão retorna um float
print('Divisão:', divisao)  # Resultado: aproximadamente 3.3333333333333335

# Divisão Inteira
divisao_inteira = 10 // 2.2  # Divisão inteira com um divisor float
print('Divisão inteira:', divisao_inteira)  # Resultado: aproximadamente 4.0 (float)

# Exponenciação
exponenciacao = 2 ** 10  # 2 elevado à 10ª potência
print('Exponenciação:', exponenciacao)  # Resultado: 1024

# Módulo
modulo = 55 % 2
print('Módulo:', modulo)  # Resultado: 1 (resto da divisão de 55 por 2)

print(10 % 2 == 0)
print(15 % 2 == 0)
